let a = 5;
let b = "5";

// Comparação frouxa (==)
if (a == b) {
    console.log("a == b: São iguais!");
} else {
    console.log("a == b: São diferentes!");
}

// Comparação estrita (===)
if (a === b) {
    console.log("a === b: São iguais!");
} else {
    console.log("a === b: São diferentes!");
}
